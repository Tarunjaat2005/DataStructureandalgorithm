package Bit_Manipulation;

public class All_Basic_operators {

	public static void main(String[] args) {
		System.out.println(5&2);//and operator  // Explanation in notes
		System.out.println(5|2);// or operator
		System.out.println(5^2);//xor Operator
		System.out.println(~5);//complement of a number
		System.out.println(5>>2);//right Shift
		System.out.println(5<<2);//left Shift

	}

}
